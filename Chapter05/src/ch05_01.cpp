#include <stdio.h>	// โปรแกรมที่ 5.1 แสดงการใช้ฟังก์ชัน printf() บังคับการพิมพ์ค่า 
main() {	
    int d = 10; 	// d คือ จำนวนเต็ม
    float f = 3.5; 	// f คือ จำนวนจริง
    char c = 'A'; 	// c คือ อักขระ 
    char s[] = "Hello";	// s คือ ข้อความ
    printf("%d\n", d);	// พิมพ์จำนวนเต็ม
    printf("%f\n", f);	// พิมพ์จำนวนจริง (ปกติ มีทศนิยม 6 ตำแหน่ง)
    printf("%.4f\n", f);	// พิมพ์จำนวนจริง (ทศนิยม 4 ตำแหน่ง)
    printf("%c\n", c);	// พิมพ์อักขระ
    printf("%s", s);	// พิมพ์ข้อความ
}	