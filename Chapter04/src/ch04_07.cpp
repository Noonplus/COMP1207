#include <iostream> // โปรแกรมที่ 4.7 แสดงวิธีการเขียนใช้งานตัวดำเนินการเพิ่มหรือลดค่า
using namespace std;
main() {
    int x;
    x = 10; cout <<  x++ << ' ' << x << endl;
    x = 10; cout <<  ++x << ' ' << x << endl;
    x = 10; cout <<  x-- << ' ' << x << endl;
    x = 10; cout <<  --x << ' ' << x ;
}