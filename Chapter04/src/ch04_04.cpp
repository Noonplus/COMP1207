#include <iostream> // โปรแกรมที่ 4.4 แสดงวิธีการเขียนใช้งานตัวดำเนินการเชิงสัมพันธ์
using namespace std;
main() {
    int a=5, b=10;
    cout << a << " < " << b << " : " << (a < b) << endl;
    cout << a << " > " << b << " : " << (a > b) << endl;
    cout << a << " == " << b << " : " << (a == b) << endl;
    cout << a << " != " << b << " : " << (a != b) << endl;
    cout << a << " <= " << b << " : " << (a <= b) << endl;
    cout << a << " >= " << b << " : " << (a >= b);
}